        <section class="etapes">
            <h1>Étapes du brassage :</h1>
            <div id="Etapes">
                <div>
                    <img src="public/img/etapes/concassage.svg" alt="Photo du concassage">
                    <p>Le concassage des grains vise à les broyer grossièrement afin d'ouvrir leur structure, favorisant ainsi l'activité enzymatique pour extraire l'amidon et les enzymes nécessaires.</p>
                </div>
                <div>
                    <img src="public/img/etapes/empatage.svg" alt="Photo de l'empatage">
                    <p>L'empâtage consiste à activer les enzymes à différents paliers de température, car ils ne sont pas tous efficaces à la même température, en mélangeant les grains concassés avec de l'eau. Cette étape transforme l'amidon en sucres simples, que les levures pourront consommer.</p>
                </div>
                <div>
                    <img src="public/img/etapes/filtration.svg" alt="Photo de la filtration">
                    <p>La filtration permet de séparer la partie soluble (le moût) de la partie insoluble (les drêches). Pour ce faire, on rince les drêches à l'eau chaude, augmentant ainsi l'efficacité de la filtration en réduisant la viscosité et permettant de récupérer le moût.</p>
                </div>
                <div>
                    <img src="public/img/etapes/ebullition.svg" alt="Photo de l'ébullition">
                    <p>L'ébullition du moût a plusieurs objectifs : détruire les micro-organismes pathogènes par la chaleur, ajouter le houblon pour aromatiser la bière, et inactiver les enzymes pour arrêter la transformation de l'amidon en sucre.</p>
                </div>
                <div>
                    <img src="public/img/etapes/clarification.svg" alt="Photo de la clarification">
                    <p>La clarification vise à séparer le houblon du moût, généralement réalisée à l'aide d'une méthode de tourbillonnement pour obtenir un moût clair et débarrassé des particules indésirables.</p>
                </div>
                <div>
                    <img src="public/img/etapes/refroidissement.svg" alt="Photo du refroidissement">
                    <p>Le refroidissement rapide du moût après l'ébullition est crucial pour limiter les risques de contamination et pour permettre l'introduction des levures dans le moût, débutant ainsi le processus de fermentation dans le fermenteur.</p>
                </div>
            </div>

